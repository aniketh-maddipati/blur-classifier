"""
Copy confirmed review rows into dataset/train and dataset/holdout.

This is a deliberate, separate step from cull_review.py. Reviewing alone
never moves any files into the training dataset.
"""

from __future__ import annotations

import argparse
import csv
import shutil
from collections import defaultdict
from pathlib import Path

from config import CLASSES, HOLDOUT_DIR, TRAIN_DIR

COMMON_REQUIRED_FIELDS = {
    "filename",
    "source_path",
    "indoor_or_outdoor",
    "focal_length",
    "zero_shot_guess",
    "agrees_with_human",
}


def parse_args() -> argparse.Namespace:
    parser = argparse.ArgumentParser(
        description="Copy confirmed rows from a review CSV into dataset/train and dataset/holdout."
    )
    parser.add_argument("review_csv", help="CSV generated by cull_review.py")
    return parser.parse_args()


def read_rows(csv_path: Path) -> list[dict[str, str]]:
    with csv_path.open(newline="") as handle:
        reader = csv.DictReader(handle)
        fieldnames = set(reader.fieldnames or [])
        missing = COMMON_REQUIRED_FIELDS.difference(fieldnames)
        if missing:
            raise SystemExit(f"Review CSV is missing required columns: {sorted(missing)}")
        if not ({"target_class", "claimed_class"} & fieldnames):
            raise SystemExit("Review CSV must include either 'target_class' or 'claimed_class'.")
        if not ({"human_decision", "human_confirmed"} & fieldnames):
            raise SystemExit("Review CSV must include either 'human_decision' or 'human_confirmed'.")
        return list(reader)


def resolved_target_class(row: dict[str, str]) -> str:
    return row.get("target_class") or row.get("claimed_class", "")


def row_is_selected(row: dict[str, str]) -> bool:
    if row.get("human_decision") in {"confirm", "reclassify"}:
        return True
    return row.get("human_confirmed") == "y"


def confirmed_rows_by_class(rows: list[dict[str, str]]) -> dict[str, list[dict[str, str]]]:
    grouped: dict[str, list[dict[str, str]]] = {cls: [] for cls in CLASSES}
    for row in rows:
        cls = resolved_target_class(row)
        if cls not in grouped:
            continue
        if row_is_selected(row):
            grouped[cls].append(row)
    return grouped


def split_rows(rows: list[dict[str, str]]) -> tuple[list[dict[str, str]], list[dict[str, str]]]:
    holdout_count = min(10, len(rows))
    if holdout_count == 0:
        return rows, []
    return rows[:-holdout_count], rows[-holdout_count:]


def copy_rows(rows: list[dict[str, str]], destination_root: Path) -> int:
    copied = 0
    for row in rows:
        source = Path(row["source_path"]).expanduser()
        if not source.exists():
            print(f"[warn] Missing source file, skipping: {source}")
            continue

        destination_dir = destination_root / resolved_target_class(row)
        destination_dir.mkdir(parents=True, exist_ok=True)
        destination = destination_dir / source.name
        shutil.copy2(source, destination)
        copied += 1
    return copied


def agreement_rate(rows: list[dict[str, str]]) -> str:
    relevant = [row for row in rows if row["agrees_with_human"] in {"true", "false", "True", "False"}]
    if not relevant:
        return "n/a"
    agreed = sum(1 for row in relevant if row["agrees_with_human"].lower() == "true")
    return f"{agreed}/{len(relevant)} = {agreed / len(relevant):.1%}"


def main() -> None:
    args = parse_args()
    csv_path = Path(args.review_csv).expanduser().resolve()
    if not csv_path.exists():
        raise SystemExit(f"Review CSV not found: {csv_path}")

    rows = read_rows(csv_path)
    grouped = confirmed_rows_by_class(rows)

    copied_counts: dict[str, dict[str, int]] = defaultdict(lambda: {"train": 0, "holdout": 0})
    all_rows_by_class: dict[str, list[dict[str, str]]] = {cls: [] for cls in CLASSES}
    for row in rows:
        cls = resolved_target_class(row)
        if cls in all_rows_by_class:
            all_rows_by_class[cls].append(row)

    for cls in CLASSES:
        train_rows, holdout_rows = split_rows(grouped[cls])
        copied_counts[cls]["train"] = copy_rows(train_rows, Path(TRAIN_DIR))
        copied_counts[cls]["holdout"] = copy_rows(holdout_rows, Path(HOLDOUT_DIR))

    print("Final counts per class per split:")
    for cls in CLASSES:
        print(
            f"  {cls:<22} train={copied_counts[cls]['train']:<3} "
            f"holdout={copied_counts[cls]['holdout']:<3}"
        )

    print("\nHuman/zero-shot agreement rate per class:")
    for cls in CLASSES:
        print(f"  {cls:<22} {agreement_rate(all_rows_by_class[cls])}")


if __name__ == "__main__":
    main()
