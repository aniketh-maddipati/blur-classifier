"""
Regenerate dataset/train and dataset/holdout from a reviewed CSV.
"""

from __future__ import annotations

import argparse
from pathlib import Path

from config import HOLDOUT_DIR, TRAIN_DIR
from split_dataset import (
    confirmed_rows_by_class,
    read_review_rows,
    rebuild_dataset_from_review_csv,
    resolved_target_class,
    row_is_selected,
)


def parse_args() -> argparse.Namespace:
    parser = argparse.ArgumentParser(
        description="Regenerate dataset/train and dataset/holdout from a review CSV."
    )
    parser.add_argument("review_csv", help="CSV generated by cull_review.py")
    parser.add_argument("--gap-seconds", type=int, default=30, help="Start a new group at gaps >= this many seconds.")
    parser.add_argument("--seed", type=int, default=42, help="Deterministic seed for shuffling groups.")
    parser.add_argument("--holdout-per-class", type=int, default=10, help="Minimum holdout images per class.")
    return parser.parse_args()


def read_rows(csv_path: Path) -> list[dict[str, str]]:
    return read_review_rows(csv_path)


def split_rows(rows: list[dict[str, str]]) -> tuple[list[dict[str, str]], list[dict[str, str]]]:
    holdout_count = min(10, len(rows))
    if holdout_count == 0:
        return rows, []
    return rows[:-holdout_count], rows[-holdout_count:]


def copy_rows(rows: list[dict[str, str]], destination_root: Path) -> int:
    raise RuntimeError("copy_rows() is no longer supported; use split_dataset.rebuild_dataset_from_review_csv().")


def main() -> None:
    args = parse_args()
    csv_path = Path(args.review_csv).expanduser().resolve()
    if not csv_path.exists():
        raise SystemExit(f"Review CSV not found: {csv_path}")

    rebuild_dataset_from_review_csv(
        csv_path,
        train_dir=Path(TRAIN_DIR),
        holdout_dir=Path(HOLDOUT_DIR),
        manifest_path=Path(TRAIN_DIR).parent / "split_manifest.csv",
        gap_seconds=args.gap_seconds,
        seed=args.seed,
        holdout_per_class=args.holdout_per_class,
    )


if __name__ == "__main__":
    main()
