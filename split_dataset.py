"""
Regenerate dataset/train and dataset/holdout from a review CSV using grouped splits.
"""

from __future__ import annotations

import argparse
import csv
import shutil
import subprocess
from collections import Counter, defaultdict
from dataclasses import dataclass
from datetime import datetime
from pathlib import Path
from typing import Callable, Iterable, Optional

from PIL import Image

from config import CLASSES, HOLDOUT_DIR, TRAIN_DIR
from exif_analyzer import ShotMetadata, extract_metadata

ARCHIVE_ROOT = Path("/Volumes/T7/photo-archive")
RAW_EXTENSIONS = {".arw", ".cr2", ".nef", ".dng"}
JPEG_EXTENSIONS = (".JPG", ".jpg", ".JPEG", ".jpeg")
KEEP_DECISIONS = {"accept", "accepted", "confirm", "keep", "kept", "reclassify"}
DROP_DECISIONS = {"reject", "rejected", "skip", "skipped"}
COMMON_REQUIRED_FIELDS = {
    "filename",
    "source_path",
    "indoor_or_outdoor",
    "focal_length",
    "zero_shot_guess",
    "agrees_with_human",
}
MANIFEST_FIELDNAMES = [
    "basename",
    "class",
    "group_id",
    "group_start_ts",
    "split",
    "seed",
    "gap_seconds",
    "jpeg_source",
    "source_session",
]


@dataclass(frozen=True)
class ReviewedImage:
    basename: str
    target_class: str
    source_path: Path
    metadata_source_path: Path
    capture_datetime: Optional[datetime]
    jpeg_source: str
    source_session: str


@dataclass(frozen=True)
class ImageGroup:
    group_id: str
    items: tuple[ReviewedImage, ...]
    start_timestamp: Optional[datetime]


@dataclass(frozen=True)
class SplitAssignment:
    group: ImageGroup
    split: str


@dataclass(frozen=True)
class SplitResult:
    assignments: tuple[SplitAssignment, ...]
    manifest_path: Path
    missing_timestamps: tuple[str, ...]


@dataclass(frozen=True)
class ConversionFailure:
    basename: str
    message: str


@dataclass(frozen=True)
class ConversionStats:
    paired_count: int
    converted_count: int
    failed: tuple[ConversionFailure, ...]


def parse_args() -> argparse.Namespace:
    parser = argparse.ArgumentParser(
        description="Regenerate dataset/train and dataset/holdout from a review CSV using grouped splits."
    )
    parser.add_argument("review_csv", help="CSV generated by the review flow.")
    parser.add_argument(
        "--gap-seconds",
        type=int,
        default=30,
        help="Start a new group at gaps >= this many seconds.",
    )
    parser.add_argument("--seed", type=int, default=42, help="Deterministic seed for shuffling groups.")
    parser.add_argument(
        "--holdout-per-class",
        type=int,
        default=10,
        help="Greedily assign groups to holdout until each class reaches at least this many images.",
    )
    return parser.parse_args()


def read_review_rows(csv_path: Path) -> list[dict[str, str]]:
    with csv_path.open(newline="") as handle:
        reader = csv.DictReader(handle)
        fieldnames = set(reader.fieldnames or [])
        missing = COMMON_REQUIRED_FIELDS.difference(fieldnames)
        if missing:
            raise SystemExit(f"Review CSV is missing required columns: {sorted(missing)}")
        if not ({"target_class", "claimed_class"} & fieldnames):
            raise SystemExit("Review CSV must include either 'target_class' or 'claimed_class'.")
        if "human_decision" not in fieldnames:
            raise SystemExit("Review CSV must include 'human_decision'.")
        return list(reader)


def resolved_target_class(row: dict[str, str]) -> str:
    return row.get("target_class") or row.get("claimed_class", "")


def _normalized_decision(row: dict[str, str]) -> str:
    return (row.get("human_decision") or "").strip().lower()


def summarize_decisions(rows: list[dict[str, str]]) -> Counter[str]:
    counts = Counter(_normalized_decision(row) or "<blank>" for row in rows)
    print("Rows per human_decision:")
    for decision, count in sorted(counts.items()):
        print(f"  {decision}: {count}")
    return counts


def row_is_selected(row: dict[str, str]) -> bool:
    decision = _normalized_decision(row)
    if decision in KEEP_DECISIONS:
        return True
    if decision in DROP_DECISIONS:
        return False
    raise SystemExit(
        "Unrecognized human_decision value(s). "
        "Please confirm which ones mean 'keep': "
        f"{sorted({_normalized_decision(r) or '<blank>' for r in [row]})}"
    )


def validate_decision_vocabulary(rows: list[dict[str, str]]) -> None:
    decisions = {_normalized_decision(row) or "<blank>" for row in rows}
    allowed = KEEP_DECISIONS | DROP_DECISIONS
    unknown = sorted(decision for decision in decisions if decision not in allowed)
    if unknown:
        raise SystemExit(
            "Unrecognized human_decision values found; refusing to guess keep semantics: "
            f"{unknown}"
        )


def confirmed_rows_by_class(rows: list[dict[str, str]]) -> dict[str, list[dict[str, str]]]:
    grouped: dict[str, list[dict[str, str]]] = {cls: [] for cls in CLASSES}
    for row in rows:
        cls = resolved_target_class(row)
        if cls in grouped and row_is_selected(row):
            grouped[cls].append(row)
    return grouped


def _extract_capture_datetime(
    source_path: Path,
    metadata_loader: Callable[[str], ShotMetadata],
) -> Optional[datetime]:
    try:
        return metadata_loader(str(source_path)).capture_datetime
    except Exception:
        return None


def _source_session_from_path(path: Path) -> str:
    for candidate in path.parents:
        if candidate.name.startswith("raw-import-"):
            return candidate.name
    return "unknown"


def _build_archive_index(archive_root: Path) -> dict[str, list[Path]]:
    index: dict[str, list[Path]] = defaultdict(list)
    for path in archive_root.rglob("*"):
        if not path.is_file():
            continue
        suffix = path.suffix.lower()
        if suffix not in RAW_EXTENSIONS and suffix not in {ext.lower() for ext in JPEG_EXTENSIONS}:
            continue
        index[path.name.lower()].append(path)
    return index


def _find_same_basename_jpeg(path: Path) -> Path | None:
    if path.exists() and path.suffix.lower() in {ext.lower() for ext in JPEG_EXTENSIONS}:
        return path
    for extension in JPEG_EXTENSIONS:
        candidate = path.with_suffix(extension)
        if candidate.exists():
            return candidate
    return None


def resolve_preferred_image_path(source_path: Path) -> Path:
    paired = _find_same_basename_jpeg(source_path)
    if paired is not None:
        return paired
    return source_path


def _resolve_via_fallback(
    *,
    basename: str,
    archive_index: dict[str, list[Path]],
    row: dict[str, str],
    metadata_loader: Callable[[str], ShotMetadata],
) -> Path:
    candidates = archive_index.get(basename.lower(), [])
    if not candidates:
        raise FileNotFoundError(f"No archive match found for basename {basename}")
    if len(candidates) == 1:
        print(f"WARNING: source_path missing/nonexistent; fell back to archive search for {basename}")
        return candidates[0]

    original_source = Path(row.get("source_path", "")).expanduser()
    original_capture = _extract_capture_datetime(original_source, metadata_loader) if original_source else None
    if original_capture is not None:
        matching = [
            candidate
            for candidate in candidates
            if _extract_capture_datetime(candidate, metadata_loader) == original_capture
        ]
        if len(matching) == 1:
            print(f"WARNING: source_path missing/nonexistent; fell back to archive search for {basename}")
            return matching[0]

    raise RuntimeError(
        f"Ambiguous basename {basename}; fallback matched multiple candidates: "
        f"{[str(candidate) for candidate in candidates]}"
    )


def _validate_no_label_conflicts(rows: list[dict[str, str]]) -> None:
    rows_by_basename: dict[str, list[dict[str, str]]] = defaultdict(list)
    for row in rows:
        basename = Path(row.get("filename") or Path(row.get("source_path", "")).name).name
        rows_by_basename[basename].append(row)

    conflicts = []
    for basename, grouped_rows in rows_by_basename.items():
        labels = {resolved_target_class(row) for row in grouped_rows}
        if len(labels) > 1:
            conflicts.append((basename, grouped_rows))

    if conflicts:
        details = []
        for basename, grouped_rows in conflicts:
            details.append(
                f"{basename}: {[{'target_class': resolved_target_class(row), 'source_path': row.get('source_path')} for row in grouped_rows]}"
            )
        raise RuntimeError("Conflicting labels for shared basename(s): " + " | ".join(details))


def _resolve_reviewed_image(
    row: dict[str, str],
    *,
    archive_index: dict[str, list[Path]],
    metadata_loader: Callable[[str], ShotMetadata],
) -> ReviewedImage:
    original_source = Path(row["source_path"]).expanduser() if row.get("source_path") else Path()
    basename_hint = Path(row.get("filename") or original_source.name).name

    resolved_source = original_source
    if not row.get("source_path") or not original_source.exists():
        resolved_source = _resolve_via_fallback(
            basename=basename_hint,
            archive_index=archive_index,
            row=row,
            metadata_loader=metadata_loader,
        )

    paired_jpeg = _find_same_basename_jpeg(resolved_source)
    if paired_jpeg is not None:
        jpeg_source = "paired_jpeg"
        image_source = paired_jpeg
    elif resolved_source.suffix.lower() in RAW_EXTENSIONS:
        jpeg_source = "sips_from_raw"
        image_source = resolved_source
    else:
        jpeg_source = "paired_jpeg"
        image_source = resolved_source

    metadata_source = resolved_source if resolved_source.suffix.lower() in RAW_EXTENSIONS else image_source
    capture_datetime = _extract_capture_datetime(metadata_source, metadata_loader)

    if jpeg_source == "sips_from_raw":
        basename = f"{resolved_source.stem}.jpg"
    else:
        basename = image_source.name

    return ReviewedImage(
        basename=basename,
        target_class=resolved_target_class(row),
        source_path=image_source,
        metadata_source_path=metadata_source,
        capture_datetime=capture_datetime,
        jpeg_source=jpeg_source,
        source_session=_source_session_from_path(metadata_source),
    )


def collect_reviewed_images(
    rows: list[dict[str, str]],
    *,
    archive_root: Path = ARCHIVE_ROOT,
    metadata_loader: Callable[[str], ShotMetadata] = extract_metadata,
) -> tuple[list[ReviewedImage], list[str]]:
    summarize_decisions(rows)
    validate_decision_vocabulary(rows)
    _validate_no_label_conflicts(rows)
    archive_index = _build_archive_index(archive_root)

    images: list[ReviewedImage] = []
    missing_timestamps: list[str] = []

    for row in rows:
        if not row_is_selected(row):
            continue
        target_class = resolved_target_class(row)
        if target_class not in CLASSES:
            continue

        image = _resolve_reviewed_image(
            row,
            archive_index=archive_index,
            metadata_loader=metadata_loader,
        )
        if image.capture_datetime is None:
            missing_timestamps.append(image.basename)
        images.append(image)

    confirmed_counts = Counter(image.target_class for image in images)
    print("Per-class confirmed counts:")
    for class_name in CLASSES:
        print(f"  {class_name}: {confirmed_counts.get(class_name, 0)}")

    return images, sorted(missing_timestamps)


def group_images(images: list[ReviewedImage], gap_seconds: int) -> list[ImageGroup]:
    timestamped = sorted(
        (image for image in images if image.capture_datetime is not None),
        key=lambda image: (image.capture_datetime, image.basename),
    )
    untimestamped = sorted(
        (image for image in images if image.capture_datetime is None),
        key=lambda image: image.basename,
    )

    groups: list[ImageGroup] = []
    current: list[ReviewedImage] = []
    current_start: Optional[datetime] = None
    previous_timestamp: Optional[datetime] = None

    def flush_current() -> None:
        nonlocal current, current_start
        if not current:
            return
        groups.append(
            ImageGroup(
                group_id=f"group_{len(groups):04d}",
                items=tuple(current),
                start_timestamp=current_start,
            )
        )
        current = []
        current_start = None

    for image in timestamped:
        if not current:
            current = [image]
            current_start = image.capture_datetime
            previous_timestamp = image.capture_datetime
            continue

        assert previous_timestamp is not None
        assert image.capture_datetime is not None
        gap = (image.capture_datetime - previous_timestamp).total_seconds()
        if gap >= gap_seconds:
            flush_current()
            current = [image]
            current_start = image.capture_datetime
        else:
            current.append(image)
        previous_timestamp = image.capture_datetime

    flush_current()

    for image in untimestamped:
        groups.append(
            ImageGroup(
                group_id=f"group_{len(groups):04d}",
                items=(image,),
                start_timestamp=None,
            )
        )

    return groups


def shuffled_groups(groups: list[ImageGroup], seed: int) -> list[ImageGroup]:
    ordered = sorted(
        groups,
        key=lambda group: (
            group.start_timestamp is None,
            group.start_timestamp or datetime.max,
            group.items[0].basename,
        ),
    )
    import random

    rng = random.Random(seed)
    rng.shuffle(ordered)
    return ordered


def assign_groups_to_splits(
    groups: list[ImageGroup],
    *,
    holdout_per_class: int,
    seed: int,
) -> list[SplitAssignment]:
    holdout_counts = Counter({cls: 0 for cls in CLASSES})
    assignments: list[SplitAssignment] = []

    for group in shuffled_groups(groups, seed):
        class_counts = Counter(item.target_class for item in group.items)
        needs_holdout = any(
            holdout_counts[cls] < holdout_per_class and class_counts[cls] > 0 for cls in CLASSES
        )
        split = "holdout" if needs_holdout else "train"
        assignments.append(SplitAssignment(group=group, split=split))
        if split == "holdout":
            holdout_counts.update(class_counts)

    return assignments


def assert_no_leakage(assignments: list[SplitAssignment]) -> None:
    groups_by_split: dict[str, set[str]] = defaultdict(set)
    basenames_by_split: dict[str, set[str]] = defaultdict(set)

    for assignment in assignments:
        groups_by_split[assignment.split].add(assignment.group.group_id)
        for item in assignment.group.items:
            basenames_by_split[assignment.split].add(item.basename)

    assert groups_by_split["train"].isdisjoint(groups_by_split["holdout"]), "A group was assigned to both splits."
    assert basenames_by_split["train"].isdisjoint(
        basenames_by_split["holdout"]
    ), "A basename was assigned to both splits."


def write_manifest(
    assignments: list[SplitAssignment],
    *,
    manifest_path: Path,
    seed: int,
    gap_seconds: int,
) -> None:
    manifest_path.parent.mkdir(parents=True, exist_ok=True)
    with manifest_path.open("w", newline="") as handle:
        writer = csv.DictWriter(handle, fieldnames=MANIFEST_FIELDNAMES)
        writer.writeheader()
        for assignment in sorted(assignments, key=lambda item: (item.group.group_id, item.split)):
            group_start_ts = ""
            if assignment.group.start_timestamp is not None:
                group_start_ts = assignment.group.start_timestamp.isoformat(timespec="microseconds")
            for image in sorted(assignment.group.items, key=lambda item: item.basename):
                writer.writerow(
                    {
                        "basename": image.basename,
                        "class": image.target_class,
                        "group_id": assignment.group.group_id,
                        "group_start_ts": group_start_ts,
                        "split": assignment.split,
                        "seed": str(seed),
                        "gap_seconds": str(gap_seconds),
                        "jpeg_source": image.jpeg_source,
                        "source_session": image.source_session,
                    }
                )


def read_manifest(manifest_path: Path) -> list[dict[str, str]]:
    with manifest_path.open(newline="") as handle:
        return list(csv.DictReader(handle))


def wipe_split_dirs(train_dir: Path, holdout_dir: Path, extracted_dir: Path) -> None:
    for split_dir in (train_dir, holdout_dir):
        if split_dir.exists():
            shutil.rmtree(split_dir)
        for class_name in CLASSES:
            (split_dir / class_name).mkdir(parents=True, exist_ok=True)
    if extracted_dir.exists() and not extracted_dir.is_dir():
        extracted_dir.unlink()
    extracted_dir.mkdir(parents=True, exist_ok=True)


def _run_command(
    args: list[str],
    *,
    runner: Callable[..., subprocess.CompletedProcess[str]] = subprocess.run,
) -> subprocess.CompletedProcess[str]:
    return runner(args, check=True, capture_output=True, text=True)


def _validate_jpeg_dimensions(path: Path) -> None:
    with Image.open(path) as image:
        image.load()
        if max(image.size) < 5000:
            raise RuntimeError(
                f"Converted JPEG appears undersized ({image.size[0]}x{image.size[1]}) for {path.name}; "
                "sips likely fell back to a preview."
            )


def _copy_exif_tags(
    source_path: Path,
    destination_path: Path,
    *,
    runner: Callable[..., subprocess.CompletedProcess[str]] = subprocess.run,
) -> None:
    _run_command(
        [
            "exiftool",
            "-TagsFromFile",
            str(source_path),
            "-DateTimeOriginal",
            "-SubSecTimeOriginal",
            "-ExposureTime",
            "-FNumber",
            "-ISO",
            "-FocalLength",
            "-overwrite_original",
            str(destination_path),
        ],
        runner=runner,
    )


def _assert_matching_capture_timestamp(
    source_path: Path,
    destination_path: Path,
    *,
    metadata_loader: Callable[[str], ShotMetadata],
) -> None:
    source_ts = _extract_capture_datetime(source_path, metadata_loader)
    destination_ts = _extract_capture_datetime(destination_path, metadata_loader)
    if source_ts != destination_ts:
        raise RuntimeError(
            f"Converted JPEG capture timestamp does not match source RAW for {source_path.name}: "
            f"{source_ts!r} != {destination_ts!r}"
        )


def convert_raw_to_jpeg(
    source_path: Path,
    *,
    extracted_dir: Path,
    metadata_loader: Callable[[str], ShotMetadata],
    runner: Callable[..., subprocess.CompletedProcess[str]] = subprocess.run,
) -> Path:
    destination_path = extracted_dir / f"{source_path.stem}.jpg"
    if destination_path.exists() and destination_path.stat().st_mtime >= source_path.stat().st_mtime:
        _validate_jpeg_dimensions(destination_path)
        return destination_path

    _run_command(
        [
            "sips",
            "-s",
            "format",
            "jpeg",
            "-s",
            "formatOptions",
            "95",
            str(source_path),
            "--out",
            str(destination_path),
        ],
        runner=runner,
    )
    _copy_exif_tags(source_path, destination_path, runner=runner)
    _validate_jpeg_dimensions(destination_path)
    _assert_matching_capture_timestamp(
        source_path,
        destination_path,
        metadata_loader=metadata_loader,
    )
    return destination_path


def _preflight_archive_root(archive_root: Path) -> None:
    if not archive_root.exists() or not any(archive_root.iterdir()):
        raise SystemExit("Archive root is missing or empty; check that T7 is mounted.")


def _preflight_exiftool() -> None:
    if shutil.which("exiftool") is None:
        raise SystemExit("exiftool is required on PATH. Hint: brew install exiftool")


def resolve_jpeg_sources(
    images: list[ReviewedImage],
    *,
    extracted_dir: Path,
    archive_root: Path,
    metadata_loader: Callable[[str], ShotMetadata],
    runner: Callable[..., subprocess.CompletedProcess[str]] = subprocess.run,
) -> tuple[dict[str, Path], ConversionStats]:
    _preflight_archive_root(archive_root)
    raw_only = [image for image in images if image.jpeg_source == "sips_from_raw"]
    paired = [image for image in images if image.jpeg_source == "paired_jpeg"]
    print(
        f"Planned image sourcing: {len(paired)} paired JPEG, "
        f"{len(raw_only)} RAW-to-JPEG conversion candidates."
    )

    if raw_only:
        _preflight_exiftool()
        first_output = convert_raw_to_jpeg(
            raw_only[0].source_path,
            extracted_dir=extracted_dir,
            metadata_loader=metadata_loader,
            runner=runner,
        )
        with Image.open(first_output) as first_image:
            first_image.load()

    resolved_sources: dict[str, Path] = {}
    failures: list[ConversionFailure] = []
    converted_count = 0
    paired_count = 0

    for index, image in enumerate(raw_only, start=1):
        try:
            output = convert_raw_to_jpeg(
                image.source_path,
                extracted_dir=extracted_dir,
                metadata_loader=metadata_loader,
                runner=runner,
            )
            resolved_sources[image.basename] = output
            converted_count += 1
        except Exception as exc:
            failures.append(ConversionFailure(basename=image.basename, message=str(exc)))
        if index % 10 == 0 or index == len(raw_only):
            print(f"Converted {index}/{len(raw_only)} RAW-only images")

    for image in paired:
        try:
            _validate_jpeg_dimensions(image.source_path)
            resolved_sources[image.basename] = image.source_path
            paired_count += 1
        except Exception as exc:
            failures.append(ConversionFailure(basename=image.basename, message=str(exc)))

    return resolved_sources, ConversionStats(
        paired_count=paired_count,
        converted_count=converted_count,
        failed=tuple(failures),
    )


def copy_from_manifest(
    manifest_rows: list[dict[str, str]],
    *,
    source_by_basename: dict[str, Path],
    train_dir: Path,
    holdout_dir: Path,
) -> None:
    destination_roots = {"train": train_dir, "holdout": holdout_dir}
    unresolved: list[str] = []
    for row in manifest_rows:
        basename = row["basename"]
        target_class = row["class"]
        split = row["split"]
        source_path = source_by_basename.get(basename)
        if source_path is None or not source_path.exists():
            unresolved.append(basename)
            continue
        destination = destination_roots[split] / target_class / basename
        shutil.copy2(source_path, destination)

    if unresolved:
        raise RuntimeError(
            "Unable to resolve/copy the following basenames: " + ", ".join(sorted(unresolved))
        )


def _counts_by_split(assignments: list[SplitAssignment]) -> dict[str, Counter[str]]:
    counts = {
        "train": Counter({cls: 0 for cls in CLASSES}),
        "holdout": Counter({cls: 0 for cls in CLASSES}),
    }
    for assignment in assignments:
        counts[assignment.split].update(item.target_class for item in assignment.group.items)
    return counts


def build_report(
    assignments: list[SplitAssignment],
    *,
    holdout_per_class: int,
    conversion_stats: ConversionStats,
) -> str:
    counts = _counts_by_split(assignments)
    groups_per_split = Counter(assignment.split for assignment in assignments)
    largest_group_size = max((len(assignment.group.items) for assignment in assignments), default=0)

    lines = ["Final counts per class per split:"]
    for cls in CLASSES:
        lines.append(
            f"  {cls:<22} train={counts['train'][cls]:<3} holdout={counts['holdout'][cls]:<3}"
        )
        if counts["holdout"][cls] < holdout_per_class:
            lines.append(
                f"WARNING: class {cls} did not reach the holdout target of {holdout_per_class}; "
                f"best effort produced {counts['holdout'][cls]}."
            )

    lines.append("")
    lines.append(
        f"Groups: train={groups_per_split['train']} holdout={groups_per_split['holdout']} "
        f"largest_group_size={largest_group_size}"
    )
    lines.append(
        f"JPEG sourcing: paired={conversion_stats.paired_count} "
        f"converted={conversion_stats.converted_count} failed={len(conversion_stats.failed)}"
    )
    if conversion_stats.failed:
        lines.append(
            "Failed basenames: "
            + ", ".join(f"{failure.basename} ({failure.message})" for failure in conversion_stats.failed)
        )

    holdout_counts = counts["holdout"]
    for assignment in assignments:
        if assignment.split != "holdout":
            continue
        group_counts = Counter(item.target_class for item in assignment.group.items)
        for cls, class_count in group_counts.items():
            total_holdout = holdout_counts[cls]
            if total_holdout and class_count / total_holdout > 0.4:
                lines.append(
                    f"WARNING: holdout group {assignment.group.group_id} contributes {class_count}/{total_holdout} "
                    f"({class_count / total_holdout:.1%}) of class {cls}."
                )

    return "\n".join(lines)


def _print_missing_timestamp_warning(missing_timestamps: list[str]) -> None:
    if not missing_timestamps:
        return
    print("WARNING: Missing or unparseable EXIF capture timestamp; assigned singleton groups for:")
    for basename in missing_timestamps:
        print(f"  - {basename}")


def assert_dataset_jpeg_integrity(
    manifest_rows: list[dict[str, str]],
    *,
    train_dir: Path,
    holdout_dir: Path,
) -> None:
    basenames_by_split: dict[str, set[str]] = defaultdict(set)
    groups_by_split: dict[str, set[str]] = defaultdict(set)
    for row in manifest_rows:
        destination = (train_dir if row["split"] == "train" else holdout_dir) / row["class"] / row["basename"]
        if destination.suffix.lower() != ".jpg":
            raise AssertionError(f"Non-JPEG file leaked into dataset: {destination}")
        if not destination.exists():
            raise AssertionError(f"Manifest row missing on disk: {destination}")
        basenames_by_split[row["split"]].add(row["basename"])
        groups_by_split[row["split"]].add(row["group_id"])

    if not basenames_by_split["train"].isdisjoint(basenames_by_split["holdout"]):
        raise AssertionError("A basename was assigned to both splits.")
    if not groups_by_split["train"].isdisjoint(groups_by_split["holdout"]):
        raise AssertionError("A group was assigned to both splits.")

    for split_dir in (train_dir, holdout_dir):
        for path in split_dir.rglob("*"):
            if path.is_file() and path.suffix.lower() != ".jpg":
                raise AssertionError(f"Non-JPEG file found in rebuilt dataset: {path}")


def rebuild_dataset_from_rows(
    rows: list[dict[str, str]],
    *,
    train_dir: Path,
    holdout_dir: Path,
    manifest_path: Path,
    gap_seconds: int = 30,
    seed: int = 42,
    holdout_per_class: int = 10,
    metadata_loader: Callable[[str], ShotMetadata] = extract_metadata,
    archive_root: Path = ARCHIVE_ROOT,
    extracted_dir: Path | None = None,
    runner: Callable[..., subprocess.CompletedProcess[str]] = subprocess.run,
) -> SplitResult:
    if extracted_dir is None:
        extracted_dir = train_dir.parent / "_extracted"
    if extracted_dir.exists() and not extracted_dir.is_dir():
        extracted_dir.unlink()
    extracted_dir.mkdir(parents=True, exist_ok=True)

    images, missing_timestamps = collect_reviewed_images(
        rows,
        archive_root=archive_root,
        metadata_loader=metadata_loader,
    )
    groups = group_images(images, gap_seconds)
    assignments = assign_groups_to_splits(groups, holdout_per_class=holdout_per_class, seed=seed)
    assert_no_leakage(assignments)

    write_manifest(assignments, manifest_path=manifest_path, seed=seed, gap_seconds=gap_seconds)
    resolved_sources, conversion_stats = resolve_jpeg_sources(
        images,
        extracted_dir=extracted_dir,
        archive_root=archive_root,
        metadata_loader=metadata_loader,
        runner=runner,
    )
    if conversion_stats.failed:
        raise RuntimeError(
            "Failed to source JPEGs for: "
            + "; ".join(f"{failure.basename}: {failure.message}" for failure in conversion_stats.failed)
        )

    manifest_rows = read_manifest(manifest_path)
    wipe_split_dirs(train_dir, holdout_dir, extracted_dir)
    copy_from_manifest(
        manifest_rows,
        source_by_basename=resolved_sources,
        train_dir=train_dir,
        holdout_dir=holdout_dir,
    )
    assert_dataset_jpeg_integrity(manifest_rows, train_dir=train_dir, holdout_dir=holdout_dir)

    _print_missing_timestamp_warning(missing_timestamps)
    print(build_report(assignments, holdout_per_class=holdout_per_class, conversion_stats=conversion_stats))

    return SplitResult(
        assignments=tuple(assignments),
        manifest_path=manifest_path,
        missing_timestamps=tuple(missing_timestamps),
    )


def rebuild_dataset_from_review_csv(
    review_csv: Path,
    *,
    train_dir: Path = Path(TRAIN_DIR),
    holdout_dir: Path = Path(HOLDOUT_DIR),
    manifest_path: Optional[Path] = None,
    gap_seconds: int = 30,
    seed: int = 42,
    holdout_per_class: int = 10,
    metadata_loader: Callable[[str], ShotMetadata] = extract_metadata,
    archive_root: Path = ARCHIVE_ROOT,
    extracted_dir: Path | None = None,
    runner: Callable[..., subprocess.CompletedProcess[str]] = subprocess.run,
) -> SplitResult:
    if manifest_path is None:
        manifest_path = train_dir.parent / "split_manifest.csv"
    rows = read_review_rows(review_csv)
    return rebuild_dataset_from_rows(
        rows,
        train_dir=train_dir,
        holdout_dir=holdout_dir,
        manifest_path=manifest_path,
        gap_seconds=gap_seconds,
        seed=seed,
        holdout_per_class=holdout_per_class,
        metadata_loader=metadata_loader,
        archive_root=archive_root,
        extracted_dir=extracted_dir,
        runner=runner,
    )


def main() -> None:
    args = parse_args()
    review_csv = Path(args.review_csv).expanduser().resolve()
    if not review_csv.exists():
        raise SystemExit(f"Review CSV not found: {review_csv}")

    rebuild_dataset_from_review_csv(
        review_csv,
        gap_seconds=args.gap_seconds,
        seed=args.seed,
        holdout_per_class=args.holdout_per_class,
    )


if __name__ == "__main__":
    main()
