"""
Regenerate dataset/train and dataset/holdout from a reviewed CSV using grouped splits.

Every image copied into dataset/ is guaranteed to be a full-resolution JPEG:
RAW-only rows are resolved via jpeg_resolution (paired JPEG preferred, else a
validated `sips` conversion, else a hard error listing every unresolved file).
Burst grouping always uses EXIF capture timestamps read from the CSV's
source_path (the RAW file) — never the CSV's review-time columns.
"""

from __future__ import annotations

import argparse
import csv
import random
import shutil
from collections import Counter, defaultdict
from dataclasses import dataclass
from datetime import datetime
from pathlib import Path
from typing import Callable, Optional

from config import ARCHIVE_ROOT, CLASSES, EXTRACTED_DIR, HOLDOUT_DIR, TRAIN_DIR
from exif_analyzer import ShotMetadata, extract_metadata
from jpeg_resolution import (
    JpegResolver,
    LabelConflictError,
    RAW_EXTENSIONS,
    ResolutionError,
    enumerate_and_validate_decisions,
)

COMMON_REQUIRED_FIELDS = {
    "filename",
    "source_path",
    "indoor_or_outdoor",
    "focal_length",
    "zero_shot_guess",
    "agrees_with_human",
}
MANIFEST_FIELDNAMES = [
    "basename",
    "class",
    "group_id",
    "group_start_ts",
    "split",
    "seed",
    "gap_seconds",
    "jpeg_source",
    "source_session",
    "pixel_size",
]

MIN_RECOMMENDED_PER_CLASS = 20


@dataclass(frozen=True)
class ReviewedImage:
    basename: str
    target_class: str
    source_path: Path
    capture_datetime: Optional[datetime]
    jpeg_source: str = ""
    source_session: str = ""
    pixel_size: str = ""


@dataclass(frozen=True)
class ImageGroup:
    group_id: str
    items: tuple[ReviewedImage, ...]
    start_timestamp: Optional[datetime]


@dataclass(frozen=True)
class SplitAssignment:
    group: ImageGroup
    split: str


@dataclass(frozen=True)
class SplitResult:
    assignments: tuple[SplitAssignment, ...]
    manifest_path: Path
    missing_timestamps: tuple[str, ...]


def parse_args() -> argparse.Namespace:
    parser = argparse.ArgumentParser(
        description="Regenerate dataset/train and dataset/holdout from a review CSV using grouped splits."
    )
    parser.add_argument("review_csv", help="CSV generated by the review flow.")
    parser.add_argument("--gap-seconds", type=int, default=30, help="Start a new group at gaps >= this many seconds.")
    parser.add_argument("--seed", type=int, default=42, help="Deterministic seed for shuffling groups.")
    parser.add_argument(
        "--holdout-per-class",
        type=int,
        default=10,
        help="Greedily assign groups to holdout until each class reaches at least this many images.",
    )
    parser.add_argument(
        "--archive-root",
        default=ARCHIVE_ROOT,
        help="Root of the nested RAW archive, used for preflight and fallback basename search.",
    )
    parser.add_argument(
        "--extracted-dir",
        default=EXTRACTED_DIR,
        help="Cache directory for JPEGs converted from RAW files.",
    )
    return parser.parse_args()


def read_review_rows(csv_path: Path) -> list[dict[str, str]]:
    with csv_path.open(newline="") as handle:
        reader = csv.DictReader(handle)
        fieldnames = set(reader.fieldnames or [])
        missing = COMMON_REQUIRED_FIELDS.difference(fieldnames)
        if missing:
            raise SystemExit(f"Review CSV is missing required columns: {sorted(missing)}")
        if not ({"target_class", "claimed_class"} & fieldnames):
            raise SystemExit("Review CSV must include either 'target_class' or 'claimed_class'.")
        if not ({"human_decision", "human_confirmed"} & fieldnames):
            raise SystemExit("Review CSV must include either 'human_decision' or 'human_confirmed'.")
        return list(reader)


def resolved_target_class(row: dict[str, str]) -> str:
    return row.get("target_class") or row.get("claimed_class", "")


def row_is_selected(row: dict[str, str]) -> bool:
    if row.get("human_decision") in {"confirm", "reclassify"}:
        return True
    return row.get("human_confirmed") == "y"


def confirmed_rows_by_class(rows: list[dict[str, str]]) -> dict[str, list[dict[str, str]]]:
    grouped: dict[str, list[dict[str, str]]] = {cls: [] for cls in CLASSES}
    for row in rows:
        cls = resolved_target_class(row)
        if cls in grouped and row_is_selected(row):
            grouped[cls].append(row)
    return grouped


def _selected_rows(rows: list[dict[str, str]]) -> list[dict[str, str]]:
    return [
        row
        for row in rows
        if row_is_selected(row) and resolved_target_class(row) in CLASSES
    ]


def _extract_capture_datetime(source_path: Path, metadata_loader: Callable[[str], ShotMetadata]) -> Optional[datetime]:
    try:
        return metadata_loader(str(source_path)).capture_datetime
    except Exception:
        return None


def collect_reviewed_images(
    rows: list[dict[str, str]],
    metadata_loader: Callable[[str], ShotMetadata] = extract_metadata,
    resolver: Optional[JpegResolver] = None,
) -> tuple[list[ReviewedImage], list[str]]:
    # Single pass: dedupe re-reviews (same image, same label) and hard-error
    # on any basename stem that carries conflicting labels.
    label_by_stem: dict[str, str] = {}
    conflicts: dict[str, set[str]] = {}
    selected: list[dict[str, str]] = []
    duplicates = 0
    for row in _selected_rows(rows):
        stem = Path(row.get("filename") or row["source_path"]).stem.upper()
        label = resolved_target_class(row)
        if stem not in label_by_stem:
            label_by_stem[stem] = label
            selected.append(row)
        elif label_by_stem[stem] == label:
            duplicates += 1
        else:
            conflicts.setdefault(stem, {label_by_stem[stem]}).add(label)
    if conflicts:
        raise LabelConflictError(
            f"Conflicting target_class labels: { {stem: sorted(labels) for stem, labels in conflicts.items()} }"
        )
    if not selected:
        raise ResolutionError("No confirmed rows after human_decision filtering.")
    if duplicates:
        print(f"NOTE: dropped {duplicates} duplicate review rows (same image, same label).")

    images: list[ReviewedImage] = []
    missing_timestamps: list[str] = []
    unresolved: list[tuple[str, str]] = []

    for row in selected:
        target_class = resolved_target_class(row)
        source_path = Path(row["source_path"]).expanduser()

        # Burst grouping uses EXIF DateTimeOriginal from the CSV's source_path
        # (the RAW file) — never the CSV's review-time timestamp columns.
        capture_datetime = _extract_capture_datetime(source_path, metadata_loader)

        jpeg_source = ""
        source_session = ""
        pixel_size = ""
        if resolver is not None:
            try:
                resolved = resolver.resolve(source_path)
            except ResolutionError as error:
                unresolved.append((source_path.name, str(error)))
                continue
            source_path = resolved.jpeg_path
            jpeg_source = resolved.jpeg_source
            source_session = resolved.source_session
            pixel_size = resolved.pixel_size

        # Destination names are normalized to a lowercase .jpg extension so
        # dataset/ contents and the manifest are uniformly *.jpg.
        basename = (
            f"{source_path.stem}.jpg"
            if resolver is not None
            else Path(row.get("filename") or source_path.name).name
        )
        if capture_datetime is None:
            missing_timestamps.append(basename)

        images.append(
            ReviewedImage(
                basename=basename,
                target_class=target_class,
                source_path=source_path,
                capture_datetime=capture_datetime,
                jpeg_source=jpeg_source,
                source_session=source_session,
                pixel_size=pixel_size,
            )
        )

    if unresolved:
        listing = "\n".join(f"  {name}: {message}" for name, message in unresolved)
        raise ResolutionError(f"{len(unresolved)} unresolved basenames:\n{listing}")

    return images, sorted(missing_timestamps)


def group_images(images: list[ReviewedImage], gap_seconds: int) -> list[ImageGroup]:
    timestamped = sorted(
        (image for image in images if image.capture_datetime is not None),
        key=lambda image: (image.capture_datetime, image.basename),
    )
    untimestamped = sorted(
        (image for image in images if image.capture_datetime is None),
        key=lambda image: image.basename,
    )

    groups: list[ImageGroup] = []
    current: list[ReviewedImage] = []
    current_start: Optional[datetime] = None
    previous_timestamp: Optional[datetime] = None

    def flush_current() -> None:
        nonlocal current, current_start
        if not current:
            return
        groups.append(
            ImageGroup(
                group_id=f"group_{len(groups):04d}",
                items=tuple(current),
                start_timestamp=current_start,
            )
        )
        current = []
        current_start = None

    for image in timestamped:
        if not current:
            current = [image]
            current_start = image.capture_datetime
            previous_timestamp = image.capture_datetime
            continue

        assert previous_timestamp is not None
        assert image.capture_datetime is not None
        gap = (image.capture_datetime - previous_timestamp).total_seconds()
        if gap >= gap_seconds:
            flush_current()
            current = [image]
            current_start = image.capture_datetime
        else:
            current.append(image)
        previous_timestamp = image.capture_datetime

    flush_current()

    for image in untimestamped:
        groups.append(
            ImageGroup(
                group_id=f"group_{len(groups):04d}",
                items=(image,),
                start_timestamp=None,
            )
        )

    return groups


def shuffled_groups(groups: list[ImageGroup], seed: int) -> list[ImageGroup]:
    ordered = sorted(
        groups,
        key=lambda group: (
            group.start_timestamp is None,
            group.start_timestamp or datetime.max,
            group.items[0].basename,
        ),
    )
    rng = random.Random(seed)
    rng.shuffle(ordered)
    return ordered


def assign_groups_to_splits(
    groups: list[ImageGroup],
    *,
    holdout_per_class: int,
    seed: int,
) -> list[SplitAssignment]:
    holdout_counts = Counter({cls: 0 for cls in CLASSES})
    assignments: list[SplitAssignment] = []

    for group in shuffled_groups(groups, seed):
        class_counts = Counter(item.target_class for item in group.items)
        needs_holdout = any(holdout_counts[cls] < holdout_per_class and class_counts[cls] > 0 for cls in CLASSES)
        split = "holdout" if needs_holdout else "train"
        assignments.append(SplitAssignment(group=group, split=split))
        if split == "holdout":
            holdout_counts.update(class_counts)

    return assignments


def assert_no_leakage(assignments: list[SplitAssignment]) -> None:
    groups_by_split: dict[str, set[str]] = defaultdict(set)
    basenames_by_split: dict[str, set[str]] = defaultdict(set)

    for assignment in assignments:
        groups_by_split[assignment.split].add(assignment.group.group_id)
        for item in assignment.group.items:
            basenames_by_split[assignment.split].add(item.basename)

    assert groups_by_split["train"].isdisjoint(groups_by_split["holdout"]), "A group was assigned to both splits."
    assert basenames_by_split["train"].isdisjoint(
        basenames_by_split["holdout"]
    ), "A basename was assigned to both splits."


def write_manifest(
    assignments: list[SplitAssignment],
    *,
    manifest_path: Path,
    seed: int,
    gap_seconds: int,
) -> None:
    manifest_path.parent.mkdir(parents=True, exist_ok=True)
    with manifest_path.open("w", newline="") as handle:
        writer = csv.DictWriter(handle, fieldnames=MANIFEST_FIELDNAMES)
        writer.writeheader()
        for assignment in sorted(assignments, key=lambda item: (item.group.group_id, item.split)):
            group_start_ts = ""
            if assignment.group.start_timestamp is not None:
                group_start_ts = assignment.group.start_timestamp.isoformat(timespec="microseconds")
            for image in sorted(assignment.group.items, key=lambda item: item.basename):
                writer.writerow(
                    {
                        "basename": image.basename,
                        "class": image.target_class,
                        "group_id": assignment.group.group_id,
                        "group_start_ts": group_start_ts,
                        "split": assignment.split,
                        "seed": str(seed),
                        "gap_seconds": str(gap_seconds),
                        "jpeg_source": image.jpeg_source,
                        "source_session": image.source_session,
                        "pixel_size": image.pixel_size,
                    }
                )


def read_manifest(manifest_path: Path) -> list[dict[str, str]]:
    with manifest_path.open(newline="") as handle:
        return list(csv.DictReader(handle))


def wipe_split_dirs(train_dir: Path, holdout_dir: Path) -> None:
    for split_dir in (train_dir, holdout_dir):
        if split_dir.exists():
            shutil.rmtree(split_dir)
        for class_name in CLASSES:
            (split_dir / class_name).mkdir(parents=True, exist_ok=True)


def copy_from_manifest(
    manifest_rows: list[dict[str, str]],
    *,
    source_by_basename: dict[str, Path],
    train_dir: Path,
    holdout_dir: Path,
) -> None:
    destination_roots = {
        "train": train_dir,
        "holdout": holdout_dir,
    }
    for row in manifest_rows:
        basename = row["basename"]
        target_class = row["class"]
        split = row["split"]
        source_path = source_by_basename[basename]
        if source_path.suffix.lower() in RAW_EXTENSIONS:
            raise ResolutionError(
                f"Refusing to copy RAW file {source_path} into dataset/; "
                "resolve it to a JPEG first (see jpeg_resolution.py)."
            )
        if not source_path.exists():
            raise FileNotFoundError(f"Cannot copy {basename}: missing source file {source_path}")
        destination = destination_roots[split] / target_class / basename
        shutil.copy2(source_path, destination)


def assert_dataset_matches_manifest(
    manifest_rows: list[dict[str, str]],
    *,
    train_dir: Path,
    holdout_dir: Path,
) -> None:
    """Post-rebuild invariants: every manifest file exists on disk, and the
    split directories contain nothing but .jpg files."""
    destination_roots = {"train": train_dir, "holdout": holdout_dir}
    for row in manifest_rows:
        destination = destination_roots[row["split"]] / row["class"] / row["basename"]
        assert destination.exists(), f"Manifest entry missing on disk: {destination}"

    offenders = [
        path
        for split_dir in destination_roots.values()
        for path in split_dir.rglob("*")
        if path.is_file() and path.suffix.lower() != ".jpg"
    ]
    assert not offenders, f"Non-JPEG files in dataset splits: {offenders}"


def _counts_by_split(assignments: list[SplitAssignment]) -> dict[str, Counter[str]]:
    counts = {
        "train": Counter({cls: 0 for cls in CLASSES}),
        "holdout": Counter({cls: 0 for cls in CLASSES}),
    }
    for assignment in assignments:
        counts[assignment.split].update(item.target_class for item in assignment.group.items)
    return counts


def build_report(assignments: list[SplitAssignment], *, holdout_per_class: int) -> str:
    counts = _counts_by_split(assignments)
    groups_per_split = Counter(assignment.split for assignment in assignments)
    largest_group_size = max((len(assignment.group.items) for assignment in assignments), default=0)

    lines = ["Final counts per class per split:"]
    for cls in CLASSES:
        lines.append(
            f"  {cls:<22} train={counts['train'][cls]:<3} holdout={counts['holdout'][cls]:<3}"
        )
        if counts["holdout"][cls] < holdout_per_class:
            lines.append(
                f"WARNING: class {cls} did not reach the holdout target of {holdout_per_class}; "
                f"best effort produced {counts['holdout'][cls]}."
            )
        total = counts["train"][cls] + counts["holdout"][cls]
        if total < MIN_RECOMMENDED_PER_CLASS:
            lines.append(
                f"WARNING: class {cls} has only {total} images in total "
                f"(<{MIN_RECOMMENDED_PER_CLASS}); label more before real training runs."
            )

    lines.append("")
    lines.append(
        f"Groups: train={groups_per_split['train']} holdout={groups_per_split['holdout']} largest_group_size={largest_group_size}"
    )

    holdout_counts = counts["holdout"]
    for assignment in assignments:
        if assignment.split != "holdout":
            continue
        group_counts = Counter(item.target_class for item in assignment.group.items)
        for cls, class_count in group_counts.items():
            total_holdout = holdout_counts[cls]
            if total_holdout and class_count / total_holdout > 0.4:
                lines.append(
                    f"WARNING: holdout group {assignment.group.group_id} contributes {class_count}/{total_holdout} "
                    f"({class_count / total_holdout:.1%}) of class {cls}."
                )

    return "\n".join(lines)


def _print_missing_timestamp_warning(missing_timestamps: list[str]) -> None:
    if not missing_timestamps:
        return
    print("WARNING: Missing or unparseable EXIF capture timestamp; assigned singleton groups for:")
    for basename in missing_timestamps:
        print(f"  - {basename}")


def rebuild_dataset_from_rows(
    rows: list[dict[str, str]],
    *,
    train_dir: Path,
    holdout_dir: Path,
    manifest_path: Path,
    gap_seconds: int = 30,
    seed: int = 42,
    holdout_per_class: int = 10,
    metadata_loader: Callable[[str], ShotMetadata] = extract_metadata,
    resolver: Optional[JpegResolver] = None,
) -> SplitResult:
    enumerate_and_validate_decisions(rows)

    if resolver is not None:
        resolver.preflight()
        resolver.run_canary(
            Path(row["source_path"]).expanduser() for row in _selected_rows(rows)
        )

    images, missing_timestamps = collect_reviewed_images(
        rows, metadata_loader=metadata_loader, resolver=resolver
    )
    if resolver is not None:
        print(resolver.summary())
    print(f"Confirmed images per class: {dict(Counter(image.target_class for image in images))}")

    groups = group_images(images, gap_seconds)
    assignments = assign_groups_to_splits(groups, holdout_per_class=holdout_per_class, seed=seed)
    assert_no_leakage(assignments)

    write_manifest(assignments, manifest_path=manifest_path, seed=seed, gap_seconds=gap_seconds)

    source_by_basename = {image.basename: image.source_path for image in images}
    manifest_rows = read_manifest(manifest_path)
    wipe_split_dirs(train_dir, holdout_dir)
    copy_from_manifest(
        manifest_rows,
        source_by_basename=source_by_basename,
        train_dir=train_dir,
        holdout_dir=holdout_dir,
    )
    assert_dataset_matches_manifest(
        manifest_rows, train_dir=train_dir, holdout_dir=holdout_dir
    )

    _print_missing_timestamp_warning(missing_timestamps)
    print(build_report(assignments, holdout_per_class=holdout_per_class))

    return SplitResult(
        assignments=tuple(assignments),
        manifest_path=manifest_path,
        missing_timestamps=tuple(missing_timestamps),
    )


def rebuild_dataset_from_review_csv(
    review_csv: Path,
    *,
    train_dir: Path = Path(TRAIN_DIR),
    holdout_dir: Path = Path(HOLDOUT_DIR),
    manifest_path: Optional[Path] = None,
    gap_seconds: int = 30,
    seed: int = 42,
    holdout_per_class: int = 10,
    metadata_loader: Callable[[str], ShotMetadata] = extract_metadata,
    resolver: Optional[JpegResolver] = None,
    archive_root: Path = Path(ARCHIVE_ROOT),
    extracted_dir: Path = Path(EXTRACTED_DIR),
) -> SplitResult:
    if manifest_path is None:
        manifest_path = train_dir.parent / "split_manifest.csv"
    if resolver is None:
        resolver = JpegResolver(
            archive_root, extracted_dir, metadata_loader=metadata_loader
        )
    rows = read_review_rows(review_csv)
    return rebuild_dataset_from_rows(
        rows,
        train_dir=train_dir,
        holdout_dir=holdout_dir,
        manifest_path=manifest_path,
        gap_seconds=gap_seconds,
        seed=seed,
        holdout_per_class=holdout_per_class,
        metadata_loader=metadata_loader,
        resolver=resolver,
    )


def main() -> None:
    args = parse_args()
    review_csv = Path(args.review_csv).expanduser().resolve()
    if not review_csv.exists():
        raise SystemExit(f"Review CSV not found: {review_csv}")

    rebuild_dataset_from_review_csv(
        review_csv,
        gap_seconds=args.gap_seconds,
        seed=args.seed,
        holdout_per_class=args.holdout_per_class,
        archive_root=Path(args.archive_root).expanduser(),
        extracted_dir=Path(args.extracted_dir).expanduser(),
    )


if __name__ == "__main__":
    main()